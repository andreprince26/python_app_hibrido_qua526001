# biblioteca
import math

# entrada de dados
r = float(input("Informe o raio do circulo: ").strip().replace(",","."))

# calculos
a = math.pi*(r**2)
c = 2*math.pi*r

# saida de dados
print(f"Numero do pi: {math.pi}")
print(f"Area da circunferencia: {a:.2f}")
print(f"Tamanho da circunferencia: {c:.2f}")
      
print(f"numero do pi: {math.pi}")

