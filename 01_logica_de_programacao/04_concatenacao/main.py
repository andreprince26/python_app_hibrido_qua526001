# declaraçao de variavel
nome = "Andre Luiz"
idade = 40

# concatenaçao de valores
#forma 1
print("Boa tarde, meu nome e " + nome + " e tenho " + str(idade) + " anos. ")
# forma 2
print("Boa tarde, meu nome e ", nome, "e tenho", idade, "anos.")
